// pass by value
// Pass by value digunakan untuk tipe data primitif. Perubahan di dalam fungsi tidak memengaruhi nilai asli di luar fungsi.
let age = 28;
age = 18;

let year = age;

console.log(year);

// pass by value berisi tipe data:
// number
// string
// boolean
// undefined
// null
// symbol
// bigint
