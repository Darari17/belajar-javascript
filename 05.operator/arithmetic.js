let x = 20;
let y = 5;

// addition (penambahan)
console.log(x + y);

// subtraction (pengurangan)
console.log(x - y);

// multiplication (perkalian)
console.log(x * y);

// division (pembagian)
console.log(x / y);

// modulus (sisa bagi)
console.log(x % y);

// exponentiation (perpangkatan)
console.log(x ** y);

// increment (tambah 1, wajib menggunakan let)
x++;
console.log(x);

// decrement (kurang 1, wajin menggunakan let)
y--;
console.log(y);
